/**
 * Writes database/migrations/LOCAL_ONLY_update_webhook_secret.sql
 * from EMAIL_WEBHOOK_SECRET in .env (gitignored).
 *
 *   node --env-file=.env scripts/sync-webhook-secret.mjs
 *
 * Then run that SQL in Supabase SQL Editor so status emails can auth
 * against /api/notify/email.
 */
import { writeFileSync } from 'fs';
import { join } from 'path';

const secret = (process.env.EMAIL_WEBHOOK_SECRET || '').trim();
const url = (
  process.env.EMAIL_WEBHOOK_URL ||
  `${(process.env.VITE_APP_URL || 'https://blackboxghana.com').replace(/\/$/, '')}/api/notify/email`
).trim();

if (!secret || secret === 'YOUR_WEBHOOK_SECRET') {
  console.error('Set EMAIL_WEBHOOK_SECRET in .env first.');
  process.exit(1);
}

const out = join(
  process.cwd(),
  'database',
  'migrations',
  'LOCAL_ONLY_update_webhook_secret.sql',
);

const sql = `-- LOCAL ONLY — do not commit. Matches .env EMAIL_WEBHOOK_SECRET
-- Generated by scripts/sync-webhook-secret.mjs
CREATE EXTENSION IF NOT EXISTS pg_net WITH SCHEMA extensions;

CREATE OR REPLACE FUNCTION public.webhook_notifications_to_app()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions, net
AS $$
DECLARE
  v_url    TEXT := '${url.replace(/'/g, "''")}';
  v_secret TEXT := '${secret.replace(/'/g, "''")}';
BEGIN
  PERFORM net.http_post(
    url := v_url,
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'x-bb-webhook-secret', v_secret
    ),
    body := jsonb_build_object(
      'type', TG_OP,
      'table', TG_TABLE_NAME,
      'schema', TG_TABLE_SCHEMA,
      'record', to_jsonb(NEW)
    ),
    timeout_milliseconds := 8000
  );
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_webhook_notifications_to_app ON public.notifications;
CREATE TRIGGER trg_webhook_notifications_to_app
  AFTER INSERT ON public.notifications
  FOR EACH ROW
  EXECUTE FUNCTION public.webhook_notifications_to_app();
`;

writeFileSync(out, sql, 'utf8');
console.log('Wrote', out);
console.log('URL:', url);
console.log('Next: run that SQL in Supabase SQL Editor.');
console.log('Also set SUPABASE_SERVICE_ROLE_KEY on Vercel for status-update emails.');
